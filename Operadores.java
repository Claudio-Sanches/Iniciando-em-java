
public class Operadores {

	public static void main(String[] args) {
		
		int varA = 5;
		int varB = 2;
		int varResult;
		
		System.out.println("Os números são A = 5 e B = 2");
		
		// soma
		varResult = varA + varB;
		System.out.print("O resultado de A + B é : ");
		System.out.println(varResult);
		
		// subtração
		varResult = varA - varB;
		System.out.print("O resultado de A - B é : ");
		System.out.println(varResult);
		
		// multiplicação
		varResult = varA * varB;
		System.out.print("O resultado de A * B é : ");
		System.out.println(varResult);
		
		// divisão
		varResult = varA / varB;
		System.out.print("O resultado de A / B é : ");
		System.out.println(varResult);

	}

}
