
public class OperadoresDeAtribuiçãoCompostos {

	public static void main(String[] args) {
		
		int varA = 10;
		varA += 3;
		int varB = 10;
		varB -= 3;
		int varC = 10;
		varC *= 3;
		int varD = 10;
		varD /= 3;
		
		System.out.println("Atribuido o valor a variável A = 10");
		System.out.println("Valor da variável A após atribuição composta +=3 : " + varA);
		System.out.println(" ");

		System.out.println("Atribuido o valor a variável B = 10");
		System.out.println("Valor da variável B após atribuição composta -=3 : " + varB);
		System.out.println(" ");

		System.out.println("Atribuido o valor a variável C = 10");
		System.out.println("Valor da variável C após atribuição composta *=3 : " + varC);
		System.out.println(" ");

		System.out.println("Atribuido o valor a variável D = 10");
		System.out.println("Valor da variável D após atribuição composta /=3 : " + varD);
		System.out.println(" ");


	}

}
